package com.example.employee;

public class EmployeeInfo
{
    String first_name;
    String last_name;
    String persons_age;
    String persons_weight;
    String persons_height;

    public EmployeeInfo(String first_name, String last_name, String persons_age, String persons_weight, String persons_height)
    {
        this.first_name = first_name;
        this.last_name = last_name;
        this.persons_age = persons_age;
        this.persons_weight = persons_weight;
        this.persons_height = persons_height;
    }


}
